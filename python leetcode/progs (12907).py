# Definition for singly-linked list.
class ListNode(object):
    def __init__(self, x):
        self.val = x
        self.next = None


class Solution(object):
    def swapPairs(self, head):
        """
        :type head: ListNode
        :rtype: ListNode
        """
        ph = p1 = head
        if head:
            p2 = head.next
        while p1 and p2:
            p1.val, p2.val = p2.val, p1.val
            p1 = p2.next
            if p1:
                p2 = p1.next
        return ph

    def generate_node_list(self, node_list):
        ph = head = ListNode(-1)
        for itm in node_list:
            head.next = ListNode(itm)
            head = head.next
        return ph.next

    def main(self, lists):
        res = self.generate_node_list(lists)
        return self.swapPairs(res)


