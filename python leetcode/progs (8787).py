class Solution(object):
    def generateParenthesis(self, n):
        """
        :type n: int
        :rtype: List[str]
        """
        lp = rp = n
        res = []
        self.backtrack(res, '', n, n)
        return res

    def backtrack(self, res, sub, l, r):
        if l == 0 and r == 0:
            res.append(sub)
            return 0
        if l > r:
            return 0
        if l > 0:
            self.backtrack(res, sub + "(", l-1, r)
        if r > 0:
            self.backtrack(res, sub + ")", l, r-1)


