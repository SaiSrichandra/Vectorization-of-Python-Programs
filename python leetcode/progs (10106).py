class Solution:
    """
    :type nums: List[int]
    :rtype: int
    """

    def removeDuplicates(self, nums):
        if len(nums) == 0:
            return 0
        total = 0
        for i in nums[1:]:
            if i != nums[total]:
                total += 1
                nums[total] = i
        return total + 1


