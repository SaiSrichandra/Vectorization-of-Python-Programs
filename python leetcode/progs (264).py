class Solution(object):
    def fourSum(self, nums, target):
        """
        :type nums: List[int]
        :type target: int
        :rtype: List[List[int]]
        """
        if len(nums) < 4:
            return []
        nums.sort()
        x = 0
        res = []
        while x < len(nums) - 3:
            nx = nums[x]
            for y in xrange(x+1, len(nums) - 2):
                ny = nums[y]
                starget = target - (nx + ny)
                i = y + 1
                j = len(nums) - 1
                while i < j:
                    tmp = nums[i] + nums[j]
                    if starget == tmp:
                        slist = [nx, ny, nums[i], nums[j]]
                        if slist not in res:
                            res.append(slist)
                        i += 1
                        j -= 1
                    elif starget > tmp:
                        i += 1
                    else:
                        j -= 1
            x += 1
        return res


