class Solution(object):
    def maxArea(self, height):
        """
        :type height: List[int]
        :rtype: int
        """
        cap = 0
        i = 0
        j = len(height) - 1
        while i < j:
            if height[i] < height[j]:
                cur = height[i] * (j - i)
                i += 1
            else:
                cur = height[j] * (j - i)
                j -= 1
            if cur > cap:
                cap = cur
        return cap


