class Solution(object):
    def romanToInt(self, s):
        """
        :type s: str
        :rtype: int
        """
        roman_dict = {'I': 1,
                      'V': 5,
                      'X': 10,
                      'L': 50,
                      'C': 100,
                      'D': 500,
                      'M': 1000}
        total = 0
        last = 0
        for i in s.upper():
            num = roman_dict[i]
            if num > last:
                total -= last
            else:
                total += num
            last = num
        return total

