class Solution(object):
    def removeElement(self, nums, val):
        """
        :type nums: List[int]
        :type val: int
        :rtype: int
        """
        flag = len(nums) - 1
        for i in xrange(len(nums) - 1, -1, -1):
            if nums[i] == val:
                nums[i], nums[flag] = nums[flag], nums[i]
                flag -= 1
        return flag + 1


