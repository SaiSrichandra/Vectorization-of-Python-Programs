class Solution(object):
    def change_pair(self, s):
        switches = {')': '(',
                   ']': '[',
                   '}': '{'}
        return switches.get(s, None)

    def isValid(self, s):
        """
        :type s: str
        :rtype: bool
        """
        stack = []
        in_stack = ['(', '[', '{']
        out_stack = [')', ']', '}']
        for itm in s:
            if itm in in_stack:
                stack.append(itm)
            elif itm in out_stack:
                pick = stack.pop()
                tmp = self.change_pair(itm)
                if pick != tmp:
                    return False
        return True

